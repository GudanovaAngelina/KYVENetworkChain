// newApp creates the application
func newApp(
	logger log.Logger,
	db dbm.DB,
	traceStore io.Writer,
	appOpts servertypes.AppOptions,
) servertypes.Application {
	baseappOptions := server.DefaultBaseappOptions(appOpts)

	application, err := app.New(
		logger,
		db,
		traceStore,
		true,
		appOpts,
		baseappOptions...,
	)
	if err != nil {
		panic(err)
	}
	return application
}

// appExport creates a new app (optionally at a given height) and exports state.
func appExport(
	logger log.Logger,
	db dbm.DB,
	traceStore io.Writer,
	height int64,
	forZeroHeight bool,
	jailAllowedAddrs []string,
	appOpts servertypes.AppOptions,
	modulesToExport []string,
) (servertypes.ExportedApp, error) {
	var (
		bApp *app.App
		err  error
	)

	// this check is necessary as we use the flag in x/upgrade.
	// we can exit more gracefully by checking the flag here.
	homePath, ok := appOpts.Get(flags.FlagHome).(string)
	if !ok || homePath == "" {
		return servertypes.ExportedApp{}, errors.New("application home not set")
	}

	viperAppOpts, ok := appOpts.(*viper.Viper)
	if !ok {
		return servertypes.ExportedApp{}, errors.New("appOpts is not viper.Viper")
	}

	// overwrite the FlagInvCheckPeriod
	viperAppOpts.Set(server.FlagInvCheckPeriod, 1)
	appOpts = viperAppOpts

	if height != -1 {
		bApp, err = app.New(logger, db, traceStore, false, appOpts)
		if err != nil {
			return servertypes.ExportedApp{}, err
		}

		if err := bApp.LoadHeight(height); err != nil {
			return servertypes.ExportedApp{}, err
		}
	} else {
		bApp, err = app.New(logger, db, traceStore, true, appOpts)
		if err != nil {
			return servertypes.ExportedApp{}, err
		}
	}

	return bApp.ExportAppStateAndValidators(forZeroHeight, jailAllowedAddrs, modulesToExport)
}

func infoCommand() *cobra.Command {
	cmd := &cobra.Command{
		Use:   "info",
		Short: "Transactions subcommands",
		RunE: func(cmd *cobra.Command, args []string) error {
			fmt.Println("Information about build variables:")
			fmt.Printf("Version: %s\n", version.Version)
			fmt.Printf("Denom: %s\n", globalTypes.Denom)
			fmt.Printf("Team-Foundation-Authority: %s\n", teamTypes.FOUNDATION_ADDRESS)
			fmt.Printf("Team-BCP-Authority: %s\n", teamTypes.BCP_ADDRESS)
			fmt.Printf("Team-Allocation: %s\n", formatInt(teamTypes.TEAM_ALLOCATION))
			fmt.Printf("Team-TGE: %s\n", time.Unix(int64(teamTypes.TGE), 0).String())
			return nil
		},
	}

	return cmd
}

func formatInt(number uint64) string {
	output := strconv.FormatUint(number, 10)
	startOffset := 3

	outputIndex := len(output)
	if len(output) >= 6 {
		outputIndex -= 6
		output = output[:outputIndex] + "." + output[outputIndex:]
		for outputIndex > startOffset {
			outputIndex -= 3
			output = output[:outputIndex] + "," + output[outputIndex:]
		}
	}
	return output
}
